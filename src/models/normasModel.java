public class normasModel {
    int id;
    String norma,desc;

    public normasModel() {
    }

    public normasModel(int id, String norma, String desc) {
        this.id = id;
        this.norma = norma;
        this.desc = desc;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNorma() {
        return norma;
    }

    public void setNorma(String norma) {
        this.norma = norma;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }
    
    
    
}
